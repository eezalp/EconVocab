var totalTerms = 0;
var questions = 0, question = 0;
var correct = 0, incorrect = 0;

var checkBoxes = {};

var questionSlider = null;

var settingsDiv = null, quizDiv = null, resultsDiv = null;

let correctIndex = 0;

var terms = [];
var notUsed = [];
var answer = null;


function Randrange(min, max){
  return Math.round((Math.random() * (max - 1)) + min)
}

function GenerateCheckboxes(){
  let checkboxDiv = document.getElementById("checkboxDiv");
  for(const key in vocab){
    let unitDiv = document.createElement("div");
    let title = document.createElement("h2");

    title.innerHTML = key.toString();

    unitDiv.classList.add("unitDiv");
    unitDiv.appendChild(title);
    for(let i = 0; i < vocab[key].length; i++){
      let label = document.createElement("label");
      let checkbox = document.createElement("input");
      let br = document.createElement("br");

      let name = vocab[key][i].name;
      let count = vocab[key][i].words.length;
      
      checkbox.addEventListener("change", function() {
        if(checkbox.checked){
          totalTerms += count;
          if(checkBoxes[key])
            checkBoxes[key].push(`${name}`);
          else
            checkBoxes[key] = [`${name}`];
        }else{
          // checkBoxes.delete([`${key.toString()}`, `${name}`]);
          let list = checkBoxes[key].filter(val => val != name);
          totalTerms -= count;
          if(list != []){
            checkBoxes[key] = list;
          }else{
            delete checkBoxes[key]
          }
        }
        questionSlider.max = totalTerms;
        questionSlider.value = questionSlider.value > totalTerms ? totalTerms : questionSlider.value;
        SliderChange(questionSlider.value);
      });

      checkbox.type = "checkbox";
      
      label.appendChild(checkbox);
      if(count){
        label.appendChild(document.createTextNode(`${name} (${count} terms)`));
      }else{
        checkbox.disabled = true;
        label.appendChild(document.createTextNode(`${name} (NYI)`));
      }

      unitDiv.appendChild(label);
      unitDiv.appendChild(br);
    }
    checkboxDiv.appendChild(unitDiv);
  }
}

function SwitchToQuiz(){
  if(totalTerms == 0){
    alert("No terms selected");
    return;
  }else if(totalTerms < 0){
    alert(`How tf u manage this. Total terms = ${totalTerms}`);
    return;
  }
  
  settingsDiv.hidden = true;
  quizDiv.hidden = false;
}
function SwitchToResults(){
  quizDiv.hidden = true;
  resultsDiv.hidden = false;
}

function EndQuiz(){
  SwitchToResults();
  document.getElementById("correct").innerHTML = `You got ${correct} correct of ${questions}`;
  document.getElementById("incorrect").innerHTML = `You got ${incorrect} incorrect of ${questions}`;
  document.getElementById("percent").innerHTML = `You scored a %${((correct/questions) * 100).toFixed(2)} on a quiz containing ${questions} questions`;

}

function MCQReveal(){
  for(let i = 1; i < 5; i++){
    let but = document.getElementById(`ansBut${i}`);
    but.classList.add(((i - 1) == correctIndex) ? "correctAns" : "incorrectAns");
    but.disabled = true;
  }
  setTimeout(() => {
    GenQuestion();
  }, 1500);
}

function MCQIncorrect(){
  console.log("Inorrect");
  MCQReveal();
  incorrect++;
}

function MCQCorrect(){
  console.log("Correct");
  MCQReveal();
  correct++;
}

function GenMCQDef(){

  let answers = [];
  let ansInd = Randrange(0, notUsed.length - 1);
  answer = notUsed[ansInd];
  correctIndex = Randrange(0, 3);
  while(answers.length < 3){
    let tmp = terms[Randrange(0, terms.length - 1)];
    if(tmp.word != answer.word && tmp.definition != answer.definition){
      answers.push(tmp);
    }
  }

  answers.splice(correctIndex, 0, answer);

  for(let i = 0; i < 4; i++){
    let but = document.getElementById(`ansBut${i+1}`);
    var new_but = but.cloneNode(true);
    but.parentNode.replaceChild(new_but, but);
    new_but.classList.remove("correctAns");
    new_but.classList.remove("incorrectAns");
    new_but.disabled = false;

    new_but.innerHTML = answers[i].definition;
    if(i == correctIndex){
      new_but.addEventListener("click", function() {MCQCorrect()});
    }else{
      new_but.addEventListener("click", function() {MCQIncorrect()});
    }
  }

  document.getElementById("question").innerHTML = answer.word;
  console.log(`The answer is ${answer.definition}`);
  notUsed = notUsed.filter(val => val.word != answer.word && val.definition != answer.definition);
}

function GenQuestion(){
  if(++question > questions){
    EndQuiz();
    return;
  }

  document.getElementById("questionNum").innerHTML = `${question}/${questions}`;
  GenMCQDef();
}

function GETJson(){
  fetch("Vocab.json")
    .then(res => res.json())
    .then(res => vocab = res)
    .catch(error => console.error("Error retreiving vocab", error));
  console.log(vocab);
}

function LoadQuiz(){
  SwitchToQuiz();
  terms = [];
  correct = 0;
  incorrect = 0;
  question = 0;
  questions = questionSlider.value;

  for(const key in checkBoxes){
    let chList = checkBoxes[key];
    
    for(let i = 0; i < vocab[key].length; i++){
      if(chList.includes(vocab[key][i].name)){
        for(let _ in vocab[key][i].words){
          terms.push(vocab[key][i].words[_]);
        }
      }
    }
  }

  notUsed = terms;
  GenQuestion();
}

